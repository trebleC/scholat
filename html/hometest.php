          <div class="f-richEditorText">
           <p><span style=";font-family:'Arial',sans-serif">&nbsp;&nbsp;</span><span style=";font-family:宋体">《软件项目管理》是软件工程专业的专业课程，以培养软件项目管理能力为目的，本课程以路线图的形式讲述了软件项目管理的理论、方法以及技巧，包括项目初始、项目计划、项目执行控制、项目结束。本课程将</span><span style=";font-family:'Arial',sans-serif">CDIO</span><span style=";font-family:宋体">工程教育模式引入到课堂教学中来，以实现</span><span style=";font-family:'Arial',sans-serif">“</span><span style=";font-family:宋体">做中学</span><span style=";font-family:'Arial',sans-serif">”</span><span style=";font-family:宋体">和</span><span style=";font-family:'Arial',sans-serif">“</span><span style=";font-family:宋体">基于项目的学习</span><span style=";font-family:'Arial',sans-serif">”</span><span style=";font-family:宋体">。通过贯穿始终的项目案例和情景项目展示，同时通过一个</span><span style=";font-family:'Arial',sans-serif">Web</span><span style=";font-family:宋体">项目实践</span><span style=";font-family:'Arial',sans-serif">,</span><span style=";font-family:宋体">完成了基于敏捷模型的项目开发和管理过程</span><span style=";font-family:'Arial',sans-serif">. </span><span style=";font-family:宋体">让学生切身体会软件项目管理过程，从而更好地将软件项目管理理论与实践相结合。</span></p>
